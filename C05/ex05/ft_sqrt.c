#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb > 0)
	{
		while (i * i <= nb)
		{
			if (nb == i * i)
				return (i);
			else if (i > 46340)
				return (0);
			i++;
		}
	}
	return (0);
}
